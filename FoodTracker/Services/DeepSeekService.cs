    public DeepSeekService(HttpClient httpClient)
    {
        _httpClient = httpClient;
        _httpClient.DefaultRequestHeaders.Add("Authorization", $"Bearer {ApiKey}");
    }

    public async Task<string> SendMessageAsync(string userMessage, List<(string role, string content)>? conversationHistory = null)
    {
        var messages = new List<object>
        {
            new { role = "system", content = SystemPrompt }
        };

        if (conversationHistory != null)
        {
            foreach (var (role, content) in conversationHistory)
            {
                messages.Add(new { role, content });
            }
        }

        messages.Add(new { role = "user", content = userMessage });

        var requestBody = new
        {
            model = "deepseek-chat",
            messages = messages,
            temperature = 0.7,
            max_tokens = 500
        };

        try
        {
            var json = JsonSerializer.Serialize(requestBody);
            var content = new StringContent(json, Encoding.UTF8, "application/json");
            
            var response = await _httpClient.PostAsync(ApiUrl, content);
            var responseJson = await response.Content.ReadAsStringAsync();
            
            if (!response.IsSuccessStatusCode)
            {
                return GetFallbackResponse(userMessage);
            }

            var result = JsonSerializer.Deserialize<JsonElement>(responseJson);
            
            if (result.TryGetProperty("choices", out var choices) && choices.GetArrayLength() > 0)
            {
                var firstChoice = choices[0];
                if (firstChoice.TryGetProperty("message", out var message))
                {
                    return message.GetProperty("content").GetString() ?? GetFallbackResponse(userMessage);
                }
            }

            return GetFallbackResponse(userMessage);
        }
        catch
        {
            return GetFallbackResponse(userMessage);
        }
    }

