using Microsoft.AspNetCore.Mvc;
using FoodTracker.Data;
using FoodTracker.Models;

namespace FoodTracker.Controllers;

[ApiController]
[Route("api/[controller]")]
public class ChatController : ControllerBase
{
    private readonly FoodTrackerDbContext _context;

    public ChatController(FoodTrackerDbContext context)
    {
        _context = context;
    }

    [HttpGet("dietitians")]
    public IActionResult GetDietitians()
    {
        return Ok(_context.Dietitians);
    }

    [HttpGet("messages")]
    public IActionResult GetMessages([FromQuery] int userId = 1, [FromQuery] int dietitianId = 1)
    {
        var messages = _context.ChatMessages
            .Where(m => m.UserId == userId && m.DietitianId == dietitianId)
            .OrderBy(m => m.Timestamp)
            .Select(m => new { m.Content, m.IsFromUser, m.Timestamp })
            .ToList();

        return Ok(messages);
    }

    [HttpPost("send")]
    public IActionResult SendMessage([FromBody] ChatRequest request)
    {
        var messageId = _context.ChatMessages.Count > 0 ? _context.ChatMessages.Max(m => m.Id) + 1 : 1;
        
        var userMessage = new ChatMessage
        {
            Id = messageId,
            UserId = request.UserId,
            DietitianId = request.DietitianId,
            Content = request.Message,
            IsFromUser = true,
            Role = "user",
            Timestamp = DateTime.Now
        };
        
        _context.ChatMessages.Add(userMessage);

        var aiResponse = GenerateSmartResponse(request.Message);

        var assistantMessageId = messageId + 1;
        
        var assistantMessage = new ChatMessage
        {
            Id = assistantMessageId,
            UserId = request.UserId,
            DietitianId = request.DietitianId,
            Content = aiResponse,
            IsFromUser = false,
            Role = "assistant",
            Timestamp = DateTime.Now
        };
        
        _context.ChatMessages.Add(assistantMessage);

        return Ok(new 
        { 
            userMessage = new { Content = userMessage.Content, Timestamp = userMessage.Timestamp, IsFromUser = userMessage.IsFromUser },
            assistantMessage = new { Content = assistantMessage.Content, Timestamp = assistantMessage.Timestamp, IsFromUser = assistantMessage.IsFromUser }
        });
    }

