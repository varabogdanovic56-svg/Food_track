    [HttpDelete("clear")]
    public IActionResult ClearMessages([FromQuery] int userId = 1, [FromQuery] int dietitianId = 1)
    {
        var messages = _context.ChatMessages
            .Where(m => m.UserId == userId && m.DietitianId == dietitianId)
            .ToList();
        
        foreach (var msg in messages)
        {
            _context.ChatMessages.Remove(msg);
        }
        
        return Ok(new { success = true });
    }
